import Foundation
import UIKit
extension UIColor {
    static var mjaBlue : UIColor {
        return UIColor(red: 0.0, green: 151.0 / 255.0, blue: 220.0 / 255.0, alpha: 1.0)
    }
    static var mjaOrangeRed : UIColor {
        return UIColor(red: 255.0/255.0, green: 99.0 / 255.0, blue: 71.0 / 255.0, alpha: 1.0)
    }
    static var mjaOrange : UIColor {
        return UIColor(red: 255.0/255.0, green: 165 / 255.0, blue: 0 / 255.0, alpha: 1.0)
    }
    static  var mjaLightGray: UIColor {
        return UIColor(white: 242.0 / 255.0, alpha: 1.0)
    }
    static var mjaMainGrey: UIColor {
        return UIColor(white: 118.0 / 255.0, alpha: 1.0)
    }
}
